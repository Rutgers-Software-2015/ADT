

public class Employee
{
	public String Name;
	public String Address;
	public String Position;
	public String Salary;
	public String ID;
	

	public Employee()
	{
		Name = null;
		Address = null;
		Position = null;
		Salary = null;
		ID = null;
	}
	
	public Employee(String name, String address, String position, String salary, String ident)
	{
		Name = name;
		Address = address;
		Position = position;
		Salary = salary;
		ID = ident;
	}
	
	

}
